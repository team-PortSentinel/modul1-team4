# ==========================================================
# prompt_builder.py
#
# 역할
# Parser가 정리한 Nmap 스캔 결과를
# OpenAI(Web Search)가 이해하기 쉬운 Prompt 형태로 변환한다.
# ==========================================================


class PromptBuilder:

    # ------------------------------------------------------
    # 생성자
    # parser에서 전달받은 Dictionary 데이터를 저장
    # ------------------------------------------------------
    def __init__(self, parsed_data):
        self.parsed_data = parsed_data

    # ------------------------------------------------------
    # AI에게 전달할 Prompt 생성
    # ------------------------------------------------------
    def build_prompt(self):

        prompt = """
당신은 10년 이상의 경력을 가진 침투테스트 전문가이자 보안 컨설턴트입니다.

아래는 Nmap을 이용하여 수집한 시스템 정보입니다.

Web Search를 반드시 활용하여 최신 정보를 기반으로 분석하세요.

다음 항목을 반드시 포함하여 답변하세요.

====================================================

1. 시스템 정보

2. 서비스별 취약점

3. 관련 CVE 번호

4. CVSS 점수

5. 위험도
   (Critical / High / Medium / Low)

6. 공격 가능성

7. 공개된 Exploit 존재 여부
   (Exploit-DB, GitHub PoC 등)

8. 대응 방안

9. 참고한 공식 출처
   (NVD, CVE, Exploit-DB 등)

====================================================

[Nmap Scan Result]

"""

        # --------------------------------------------------
        # Host 정보 추가
        # --------------------------------------------------

        for host in self.parsed_data["hosts"]:

            prompt += "\n========================================\n"
            prompt += "Host Information\n"
            prompt += "========================================\n"

            prompt += f"IP Address : {host['ip']}\n"
            prompt += f"Hostname   : {host['hostname']}\n"
            prompt += f"Status     : {host['status']}\n"
            prompt += f"OS         : {host['os']['name']}\n"

            prompt += "\nOpen Ports\n"
            prompt += "----------------------------------------\n"

            # ----------------------------------------------
            # 열린 포트 정보
            # ----------------------------------------------

            for port in host["ports"]:

                prompt += f"""
Port         : {port['port']}/{port['protocol']}
State        : {port['state']}
Service      : {port['service']}
Version Info : {port['fullinfo']}

----------------------------------------
"""

        # --------------------------------------------------
        # 마지막 요청사항
        # --------------------------------------------------

        prompt += """

====================================================

위 Nmap 스캔 결과를 분석하여

각 서비스(Version Info)를 기준으로

1. 취약점 분석

2. 관련 CVE 번호

3. CVSS 점수

4. 위험도
   (Critical / High / Medium / Low)

5. 해당 취약점에 대한 공개된 Exploit 존재 여부

6. 실제 공격 가능성

7. 권장 대응 방안

8. 참고한 공식 출처
   (NVD, CVE, Exploit-DB 등)

을 최신 정보를 기준으로 분석해주세요.

동일 서비스에 여러 CVE가 존재한다면
가장 위험도가 높은 취약점부터 우선순위를 정렬하여 설명해주세요.

====================================================

"""

        return prompt